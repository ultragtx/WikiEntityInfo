'''中国移動'''（ちゅうごくいどう、中：'''{{lang|zh|中国移动通信集团公司}}'''、'''チャイナ・モバイル'''）は、[[中華人民共和国]]最大の[[移動体通信]]事業者である。[[携帯電話]]の契約者数が5億人を超え、世界最大規模を誇る。

== 概要 ==
[[2000年]]には中国郵電電信総局（[[中国電信]]、チャイナ・テレコム）が固定通信事業と移動体通信事業の2社に分割が決まり、それぞれ固定通信事業は新「中国電信」、移動体通信は「中国移動（チャイナ・モバイル）」となった。

[[1997年]]に[[香港証券取引所]](コード:[http://www.hkex.com.hk/invest/index.asp?id=company/quote_page_e.asp?WidCoID=0941 0941])に上場、また[[ニューヨーク証券取引所]]（ティッカー:[http://www.nyse.com/about/listed/quickquote.html?ticker=CHL CHL]）にも上場。

現在の従業員数は約12万人。社長は王建宙（Wang Jianzhou）。英文社名は'''China Mobile Communications Corporation'''。

一部[[ボーダフォン]]が資本参加している。

2010年現在、GSM方式とTD-SCDMA方式のサービスを全国展開している。

[[2008年]]4月から、[[第三世代携帯電話]]（3G）のテストが中国8都市（[[北京市|北京]]・[[上海市|上海]]・[[天津市|天津]]・[[広州市|杭州]]・[[深セン市|深圳]]・[[秦皇島市|秦皇島]]・[[瀋陽市|瀋陽]]・[[廈門市|廈門]]）で中国独自の[[TD-SCDMA]] (Time Division Synchronous Code Division Multiple Access) 方式を使って始めた<ref>[http://www.pcworld.com/article/id,143971-pg,1/article.html China Mobile Plans 3G Tests]（英語）</ref>が、テスト状況を伝える報道は少ない。

また[[2008年]]5月には、[[第3世代移動通信システム]]（3G）の実施を目前にして3つの通信メガグループ（[[中国電信]]・[[中国聯合通信|中国聯通]]・中国移動）に集約する業界の再々編成で、[[中国鉄通]]（以前の中国鉄道通信）を併合することが先頭を切って発表されている<ref>[http://news.searchina.ne.jp/disp.cgi?y=2008&d=0523&f=business_0523_012.shtml 中国移動が中国鉄通を完全子会社化、通信業界再編で]</ref>。

[[2008年]]8月開催の[[北京オリンピック]]のローカルスポンサー（公式パートナー）となっている。

== 日本人の利用に当たり ==
{{See also|中国における携帯電話}}
* 中国移動通信のサポートコールセンターは、オペレーターが24時間対応で中国語・英語・日本語に対応している。左記3言語は、ダイヤル後のガイダンスで選択式。
* オプションサービスとして、割引国際電話、国際ローミング、電波圏外時の着信お知らせ、キャッチホン、パケット通信のセットプラン等がある。基本的にサービスの利用には、サポートセンターへ電話して申し込む必要がある。その際、SIM購入時に取得した6桁の暗証番号が必要。
* 日本の4キャリアのようにクレジットカード払い（月締めの後日請求）も可能であるが、中国国内で発行されたクレジットカードのみ取り扱いが可能。日本人は、現地の[[中國招商銀行|招商銀行]]でクレジットカードを申し込むことが出来る（管轄エリア内在住の中国人保証人の署名と日本国発行のパスポートが必要）。ただしデビットカード（銀行でカード申込み時に選択できる）では、後払い登録が出来ないので（申込み不可）注意。
* クレジットカード払いが選択できるのは、中国移動通信の店舗で新規購入した[[SIMカード]]に限る。正規店ではない携帯電話販売店などで購入したSIMカードは、購入時の名義の関係で対象外。


'''China Mobile Limited''' ({{zh|s=中国移动通信|t=中國移動通信|p=Zhōngguó Yídòng Tōngxìn}}) is a Chinese [[Government-owned corporation|state-owned]]<ref name="Economist"/> telecommunication company that provides mobile voice and multimedia services<ref name="CMCC">[http://www.chinamobile.com/en/mainland/about/profile.html Company Profile] CMCC Official Site</ref> through its nationwide [[mobile telecommunications]] network.<ref name=CM1/> The company likely remains one of the largest mobile telecommunications companies by market capitalization today, and it was named as such in March 2011.<ref>{{cite conference  | title = FT Global 500  | publisher = The Financial Times  | date = March 2011  | url = http://media.ft.com/cms/33558890-98d4-11e0-bd66-00144feab49a.pdf  | accessdate = 01/04/2012 }}</ref> It is listed on both the [[NYSE]] and the [[Hong Kong stock exchange]].<ref name=CM1>[http://www.chinamobileltd.com/about.php?menu=1 Company Profile] China Mobile Official Site</ref> 

As of November 2012, China Mobile is the [[List of mobile network operators|world's largest mobile phone operator]] by subscribers with about 703 million.<ref>[http://www.chinamobileltd.com/ir.php?menu=11 Operation Data - Customer Numbers] China Mobile Official Site</ref>

==History==
A [[Government-owned corporation|state-owned enterprise]] directly controlled by the government of the [[People's Republic of China]]<ref name="Economist">Strait deals ''[[The Economist]]'', May 7th 2009</ref> and also a [[public company]] that is listed on the NYSE and the Hong Kong stock exchanges,<ref name="CM1"/> China Mobile has dominated [[Chinese mobile phone industry|Chinese mobile services]] since its inception. As of 2010 China Mobile controls the vast majority of its domestic mobile services market with a 70% market share.<ref name=morning/> [[China Unicom]] and [[China Telecom]] have 20% and 10% shares, respectively.<ref name=morning/>

Incorporated in 1997 as China Telecom (Hong Kong) Limited,<ref name=mile>[http://www.chinamobileltd.com/about.php?menu=6 Milestone] China Mobile Official Site</ref> China Mobile was born from the 1999 break-up of [[China Telecom]].<ref>[http://books.google.com/books?id=CE2VGH7wJcYC&lpg=PA74&dq=%22China%20Mobile%22&pg=PA68#v=onepage&q&f=false Asian economic and political issues, Volume 8 (page 68)]  Frank Columbus, Nova Publishers, 2003 ('''Google Books''')</ref> (This company continues to provide mobile services, however.<ref name=morning/>)

===State control===
China Mobile likely enjoys substantial [[Protectionism|protectionist]] benefits from China's government<ref>[http://books.google.com/books?id=CE2VGH7wJcYC&lpg=PA74&dq=%22China%20Mobile%22&pg=PA54#v=onepage&q&f=false Asian Economic and Political Issues, Volume 8 (page 54)] 2003. Frank Columbus. Nova Publishers. ('''Google Books''')</ref> but also experiences frequent government intervention in its business affairs.<ref name="Asian Econ and Polit Issues">[http://books.google.com/books?id=CE2VGH7wJcYC&lpg=PA74&dq=%22China%20Mobile%22&pg=PA84#v=onepage&q&f=false Asian Economic and Political Issues, Volume 8 (page 84)] 2003. Frank Columbus. Nova Publishers. ('''Google Books''')</ref> Government control is maintained through a presumably government-owned holding company, China Mobile Communications Corporation (CMCC), that owns 100 percent ownership of China Mobile (HK) Group Limited,<ref name="gov control">[http://web.archive.org/web/20090604221551/http://www.chinamobile.com/en/mainland/about/profile.html CMCC Profile] CMCC Official Site ('''Archive.org cache''')</ref> which in turn holds over seventy percent ownership of China Mobile–the remainder being controlled by public investors.<ref name="CM1"/> Established in 2000,<ref name="gov control"/> CMCC is China Mobile Ltd's current parent company as of 2011.<ref>[http://www.bloomberg.com/news/2011-05-18/china-mobile-mtr-sjm-holdings-zte-hong-kong-stocks-preview.html China Mobile, MTR, SJM Holdings, ZTE: Hong Kong Stocks Preview] By Kana Nishizawa, bloomberg.com - May 17, 2011 5:54 PM PT</ref>

